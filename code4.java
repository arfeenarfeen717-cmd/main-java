package day2;

public class code4 {

    public static void main(String[] args) {

        // Declare multiple variables in a single line
        int a = 10, b = 20, c = 30;
        double x = 1.5, y = 2.5, z = 3.5;
        char ch1 = 'A', ch2 = 'B', ch3 = 'C';
        boolean flag1 = true, flag2 = false, flag3 = true;

        // Print all variables
        System.out.println("Integers: " + a + ", " + b + ", " + c);
        System.out.println("Doubles: " + x + ", " + y + ", " + z);
        System.out.println("Chars: " + ch1 + ", " + ch2 + ", " + ch3);
        System.out.println("Booleans: " + flag1 + ", " + flag2 + ", " + flag3);
    }
}
